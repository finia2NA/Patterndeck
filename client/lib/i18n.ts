export type TranslationKey = keyof typeof en;
type Translator = (key: TranslationKey, params?: Params) => string;
type CountUnit = 'card' | 'deck';

const dictionaries: Record<UiLocale, Partial<Record<TranslationKey, string>>> = { en, de, ja };

function interpolate(template: string, params?: Params): string {
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(params[key] ?? `{${key}}`));
}

export function detectDeviceUiLocale(): UiLocale {
  const locale = getLocales()[0]?.languageTag ?? getLocales()[0]?.languageCode;
  return normalizeUiLocale(locale);
}

export function resolveUiLocale(value: string | null | undefined): UiLocale {
  return normalizeUiLocale(value);
}

export function translate(locale: UiLocale, key: TranslationKey, params?: Params): string {
  return interpolate(dictionaries[locale][key] ?? en[key], params);
}

export function formatUnitCount(
  t: Translator,
  count: number,
  unit: CountUnit,
  options: { zeroKey?: TranslationKey; infiniteAt?: number } = {},
): string {
  if (options.zeroKey && count === 0) return t(options.zeroKey);
  if (options.infiniteAt !== undefined && count >= options.infiniteAt) {
    return t(`units.${unit}.infinite` as TranslationKey);
  }

  const unitWord = t(`units.${unit}.${count === 1 ? 'singular' : 'plural'}` as TranslationKey);
  return t('units.count', { count, unit: unitWord });
}

export function getCurrentUiLocale(): UiLocale {
  const state = useSettingsStore.getState();
  return state.hydrated ? resolveUiLocale(getLocalSetting('ui_language')) : detectDeviceUiLocale();
}

export function getAiResponseLanguage(locale: UiLocale = getCurrentUiLocale()): string {
  return UI_LOCALE_LANGUAGE_NAMES[locale];
}

export function useI18n() {
  const rawLocale = useSettingsStore(state => state.settings.ui_language ?? null);
  const hydrated = useSettingsStore(state => state.hydrated);
  const locale = hydrated ? resolveUiLocale(rawLocale) : detectDeviceUiLocale();
  return useMemo(() => ({
    locale,
    supportedLocales: UI_LOCALES,
    localeLabels: UI_LOCALE_LABELS,
    responseLanguage: UI_LOCALE_LANGUAGE_NAMES[locale],
    t: (key: TranslationKey, params?: Params) => translate(locale, key, params),
  }), [locale]);
}
